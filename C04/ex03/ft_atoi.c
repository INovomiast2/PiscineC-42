int     ft_atoi(char *str)
{
        int     i;
        int     nbr;
        int     ngv;

        i = 0;
        nbr = 0;
        ngv = 0;
        while ((str[i] == '\n') || (str[i] == '\t'
                        || (str[i] == '\v') || (str[i] == ' ')
                        || (str[i] == '\f') || (str[i] == '\r')))
                i++;
        if (str[i] == '-')
                ngv = 1;
        if (str[i] == '+' || str[i] == '-')
                i++;
}
